package corejavastringprograms;

public class ReplaceDemo {

	public static void main(String[] args) {
		
		String s1 = "Hellow World";  // Replace World with : Universe
		
		String replacedText = s1.replace("World", "Universe");
		
		System.out.println("Replaced Text is now : " + replacedText);

	}

}
