package corejavaprograms;

public class VoidMethodWithoutParameter {

	// A user-defined void method wihout parameters

	static void printWelcomeMessage() {

		System.out.println("Welcome to the Java Prgramming world !!!");

	}
	
	static void printHelloMessage() {
		
		System.out.println("Hello");
	}

	public static void main(String[] args) {

		// call the "printWelcomeMessage" method

		printWelcomeMessage();
		printHelloMessage();
		printWelcomeMessage();
		printHelloMessage();
		printWelcomeMessage();
		printWelcomeMessage();
		printWelcomeMessage();

	}

}
