package corejavaprograms;

public class IncreamentDemo {

	public static void main(String[] args) {
		
		int i=5;
		
		//post increament example
		
		int preIncreament = ++i;
		
		System.out.println("Pre-Increament (++i) : ");
		
		System.out.println("Original value of i :" + i);
		System.out.println("PretIncreament value :" + preIncreament);

	}

}
